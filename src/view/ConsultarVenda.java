    public ConsultarVenda(java.awt.Frame parent, boolean modal, Venda venda) {
        super(parent, modal);
        initComponents();
        ItemVendaDAO dao = new ItemVendaDAO();
        ProdutoDAO pDAO = new ProdutoDAO();
        ClienteDAO cDAO = new ClienteDAO();
        this.codVenda = venda.getCodigo();
        ArrayList<ItemVenda> itensBD = dao.read(codVenda);
        
        lblCodigo.setText("Venda #" + codVenda);
        tbxData.setText(venda.getData());
        tbxHora.setText(venda.getHora());
        tbxCliente.setText(cDAO.getNome(venda.getCodigoCliente()));
        switch ( venda.getFormaPagamento() )
        {
            case 0:
                tbxFormaPagamento.setText("Cartão de Crédito");
                break;
            case 1:
                tbxFormaPagamento.setText("Dinheiro");
                break;
        }
        tbxPrecoTotal.setText(String.valueOf(venda.getPrecoTotal()));
        
        modeloCarrinho = new ModeloTabelaCarrinho(itensBD);
        tbItensVenda.setModel(modeloCarrinho);
    }

